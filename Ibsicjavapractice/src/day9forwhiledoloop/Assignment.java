package day9forwhiledoloop;

public class Assignment {
//uppercased alphabet using for loop
	public static void main(String[] args) {
		System.out.println("***print Uppercased alphabet *****");
		for(char ch='A';ch<='Z';ch++) {
			System.out.print(ch+" ");
		}
		System.out.println("***print lowercased alphabets***");
		for(char ch='a';ch<='z';ch++) {
			System.out.print(ch+" ");
		} 
		

	}

}
